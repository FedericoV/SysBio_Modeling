ordered_params = ['vmax', 'km', 'k_synt_s', 'k_deg_s', 'k_deg_p']
n_vars = 2


def model(y, t, yout, p):
    #---------------------------------------------------------#
    #Parameters#
    #---------------------------------------------------------#

    vmax = p[0]
    km = p[1]
    k_synt_s = p[2]
    k_deg_s = p[3]
    k_deg_p = p[4]


    #---------------------------------------------------------#
    #Variables#
    #---------------------------------------------------------#

    _s = y[0]
    _p = y[1]


    #---------------------------------------------------------#
    #Differential Equations#
    #---------------------------------------------------------#

    yout[0] = ((-_s * vmax + (_s + km) * (-_s * k_deg_s + k_synt_s)) / (_s + km))
    yout[1] = ((-_p * k_deg_p * (_s + km) + _s * vmax) / (_s + km))
